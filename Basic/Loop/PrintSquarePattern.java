package Basic.Loop;

public class PrintSquarePattern {
    public static void main(String[] args){

      //This is code for for loop

        for(int i=1; i<=4; i++){
            System.out.println("* * * *");
        }

        //This is code for for loop

        int i = 1;
        while(i <= 4){
            System.out.println("* * * *");
            i++;
        }
    }
    
}
