package Basic.ConditionalStatement;

public class LargestOfTwoNumbers {
    public static void main(String[] args) {
        int A = 50;
        int B = 100;
        if (A >= B) {
            System.out.println("A is largest of two number");
        } else {
            System.out.println("B is largest of two number");
        }
    }

}
